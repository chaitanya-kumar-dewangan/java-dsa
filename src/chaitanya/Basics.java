package chaitanya;

public class Basics {
    public static void main(String[] args) {

        //if conditions
        int a = 10;
        if (a == 10) {
            System.out.println("Hii Java");
        }
        // while loop
        int count = 1;
        while (count != 5) {
            System.out.println(count);
            count++;
        }

        // for loop
        for (int i = 8; i != 13; i++) {
            System.out.println(i);
        }
    }
}
